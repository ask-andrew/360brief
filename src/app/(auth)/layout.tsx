import { redirect } from 'next/navigation';
import { createServerClient } from '@/lib/supabase/client';

interface AuthLayoutProps {
  children: React.ReactNode;
}

export default async function AuthLayout({ children }: AuthLayoutProps) {
  const supabase = createServerClient();
  const { data: { session } } = await supabase.auth.getSession();

  // If there's no session, redirect to login
  if (!session) {
    redirect('/login');
  }

  return (
    <div className="flex h-screen bg-gray-50">
      {/* Sidebar */}
      <aside className="w-64 bg-white border-r border-gray-200">
        <div className="p-4">
          <h1 className="text-xl font-bold text-gray-900">360Brief</h1>
          <p className="text-sm text-gray-500">Your Executive Briefing Dashboard</p>
        </div>
        <nav className="mt-6">
          <NavLink href="/app">
            <HomeIcon className="w-5 h-5" />
            <span>Dashboard</span>
          </NavLink>
          <NavLink href="/app/setup">
            <Cog6ToothIcon className="w-5 h-5" />
            <span>Set Up</span>
          </NavLink>
          <NavLink href="/app/analytics">
            <ChartBarIcon className="w-5 h-5" />
            <span>Analytics</span>
          </NavLink>
          <NavLink href="/app/billing">
            <CreditCardIcon className="w-5 h-5" />
            <span>Billing</span>
          </NavLink>
          <NavLink href="/api/auth/signout">
            <ArrowRightOnRectangleIcon className="w-5 h-5" />
            <span>Sign Out</span>
          </NavLink>
        </nav>
      </aside>

      {/* Main content */}
      <main className="flex-1 overflow-auto">
        <div className="p-8">
          {children}
        </div>
      </main>
    </div>
  );
}

// Reusable NavLink component
function NavLink({ href, children }: { href: string; children: React.ReactNode }) {
  return (
    <a
      href={href}
      className="flex items-center px-6 py-3 text-sm font-medium text-gray-600 hover:bg-gray-100 hover:text-gray-900"
    >
      {children}
    </a>
  );
}

